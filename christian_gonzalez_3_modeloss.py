# 3. AGENTE NORMALIZADOR
# ============================================================
class AgenteNormalizador:
    def __init__(self):
        self.scaler = StandardScaler()

    def descargar_dataset(self):
        path = kagglehub.dataset_download("rabieelkharoua/students-performance-dataset")
        print("Dataset descargado en:", path)
        print("Archivos encontrados:", os.listdir(path))
        return path

    def cargar_dataset(self, path):
        archivos_csv = [a for a in os.listdir(path) if a.endswith(".csv")]
        if not archivos_csv:
            raise FileNotFoundError("No se encontró ningún archivo CSV.")
        df = pd.read_csv(os.path.join(path, archivos_csv[0]))
        print("Dataset cargado correctamente. Filas y columnas:", df.shape)
        return df

    def explorar_dataset(self, df):
        print("Primeras filas del dataset:")
        display(df.head())
        print("\nInformación general del dataset:")
        df.info()
        print("\nValores nulos por columna:")
        print(df.isnull().sum())
        print("\nEstadísticas descriptivas:")
        display(df.describe())

    def graficar_distribucion_objetivo(self, df):
        fig, ax = plt.subplots(figsize=(8, 5))
        df["GradeClass"].value_counts().sort_index().plot(kind="bar", ax=ax)
        ax.set_title("Distribución de la variable objetivo GradeClass")
        ax.set_xlabel("Clase de rendimiento académico")
        ax.set_ylabel("Cantidad de estudiantes")
        plt.tight_layout()
        plt.show()
        plt.close(fig)

    def limpiar_y_normalizar(self, df):
        df = df.copy()
        if "GradeClass" not in df.columns:
            raise ValueError("No existe la columna objetivo GradeClass.")
        if "StudentID" in df.columns:
            df = df.drop("StudentID", axis=1)
        y = df["GradeClass"]
        X = df.drop("GradeClass", axis=1)
        X_norm = pd.DataFrame(self.scaler.fit_transform(X), columns=X.columns)
        X_norm["GradeClass"] = y.values
        return X_norm

