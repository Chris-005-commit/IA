# 6. CLASE PARA GENERAR REPORTE EN TXT
# ============================================================

class ReporteTXT:
    """
    Genera el reporte final como archivo de texto plano (.txt).
    Sin dependencias externas, sin graficos, sin problemas de encoding.
    """

    def crear_txt(self, reporte_final, metricas_df, nombre_mejor_modelo):
        separador = "=" * 60
        lineas = []

        # Encabezado
        lineas.append(separador)
        lineas.append("REPORTE FINAL - SISTEMA MULTIAGENTE")
        lineas.append("Prediccion del Rendimiento Estudiantil")
        lineas.append(separador)
        lineas.append("")

        # Introduccion
        lineas.append("DESCRIPCION DEL SISTEMA")
        lineas.append("-" * 40)
        lineas.append(
            "Este reporte fue generado automaticamente por el sistema multiagente. "
            "El proyecto utiliza un Agente Normalizador, un Agente Entrenador "
            "y un Agente Comunicador basado en un modelo Transformer."
        )
        lineas.append("")

        # Metricas
        lineas.append("METRICAS OBTENIDAS")
        lineas.append("-" * 40)
        for _, fila in metricas_df.iterrows():
            lineas.append(
                f"Modelo    : {fila['Modelo']}\n"
                f"Accuracy  : {fila['Accuracy']:.4f}\n"
                f"Precision : {fila['Precision']:.4f}\n"
                f"Recall    : {fila['Recall']:.4f}\n"
                f"F1 Score  : {fila['F1 Score']:.4f}"
            )
            lineas.append("")

        # Mejor modelo
        lineas.append("-" * 40)
        lineas.append(f"MEJOR MODELO SELECCIONADO: {nombre_mejor_modelo}")
        lineas.append("-" * 40)
        lineas.append("")

        # Reporte del Transformer
        lineas.append("REPORTE GENERADO POR EL AGENTE COMUNICADOR")
        lineas.append("-" * 40)
        lineas.append(reporte_final)
        lineas.append("")
        lineas.append(separador)

        contenido = "\n".join(lineas)

        nombre_txt = "reporte_final.txt"
        with open(nombre_txt, "w", encoding="utf-8") as f:
            f.write(contenido)

        return nombre_txt

