# 5. AGENTE COMUNICADOR
# ============================================================
class AgenteComunicador:
    def __init__(self):
        self.nombre_modelo_transformer = "google/flan-t5-small"
        self.tokenizer = None
        self.modelo = None
        try:
            self.tokenizer = AutoTokenizer.from_pretrained(self.nombre_modelo_transformer)
            self.modelo    = AutoModelForSeq2SeqLM.from_pretrained(self.nombre_modelo_transformer)
            print("Transformer cargado correctamente.")
        except Exception as e:
            print("No se pudo cargar el Transformer:", e)

    def generar_reporte_base(self, nombre_modelo, metricas_df):
        fila = metricas_df[metricas_df["Modelo"] == nombre_modelo].iloc[0]
        return (
            "El sistema multiagente desarrollado permite analizar el rendimiento "
            "estudiantil a partir de un dataset obtenido desde Kaggle.\n\n"
            "El Agente Normalizador se encargo de descargar el dataset, revisar "
            "valores nulos, eliminar columnas innecesarias como StudentID y normalizar "
            "las variables numericas mediante StandardScaler.\n\n"
            "El Agente Entrenador comparo tres modelos de clasificacion: Regresion "
            f"Logistica, Arbol de Decision y Random Forest. El mejor modelo fue {nombre_modelo}.\n\n"
            f"Metricas: Accuracy={fila['Accuracy']:.4f}, Precision={fila['Precision']:.4f}, "
            f"Recall={fila['Recall']:.4f}, F1 Score={fila['F1 Score']:.4f}.\n\n"
            "En conclusion, el modelo seleccionado presenta un rendimiento adecuado para "
            "clasificar el desempeno academico de los estudiantes."
        )

    def generar_reporte(self, nombre_modelo, metricas_df):
        if self.tokenizer is None or self.modelo is None:
            return self.generar_reporte_base(nombre_modelo, metricas_df)
        fila = metricas_df[metricas_df["Modelo"] == nombre_modelo].iloc[0]
        prompt = (
            "Write a short professional academic report in Spanish about a multi-agent system "
            "for student performance prediction. "
            f"Best model: {nombre_modelo}. "
            f"Accuracy: {fila['Accuracy']:.4f}. "
            f"F1 Score: {fila['F1 Score']:.4f}. "
            "Include conclusion."
        )
        try:
            entradas = self.tokenizer(prompt, return_tensors="pt", max_length=256, truncation=True)
            salida   = self.modelo.generate(**entradas, max_new_tokens=200, num_beams=2,
                                            do_sample=False, early_stopping=True)
            reporte  = self.tokenizer.decode(salida[0], skip_special_tokens=True)
            if len(reporte.strip()) < 100:
                return self.generar_reporte_base(nombre_modelo, metricas_df)
            return reporte
        except Exception as e:
            print("Error con Transformer, usando reporte base:", e)
            return self.generar_reporte_base(nombre_modelo, metricas_df)

